void DrawMap(Map* map, Resource* resource, int PlayerX, int PlayerY)
{
	int j, i;
	int MapDrawPointX, MapDrawPointY;     // 描画するマップ座標値
	int DrawMapChipNumX, DrawMapChipNumY; // 描画するマップチップの数

	// 描画するマップチップの数をセット
	DrawMapChipNumX = SCREEN_WIDTH / MAPCHIPSIZE + 2;
	DrawMapChipNumY = SCREEN_HEIGHT / MAPCHIPSIZE + 2;

	// 画面左上に描画するマップ座標をセット
	MapDrawPointX = (PlayerX / MAPCHIPSIZE) - (DrawMapChipNumX / 2 - 1);
	MapDrawPointY = (PlayerY / MAPCHIPSIZE) - (DrawMapChipNumY / 2 - 1);

	int ScrollX = -(PlayerX % MAPCHIPSIZE);
	int ScrollY = -(PlayerY % MAPCHIPSIZE);

	// マップを描く
	for (j = -1; j < DrawMapChipNumY; j++)
	{
		for (i = -1; i < DrawMapChipNumX; i++)
		{
			// 画面からはみ出た位置なら描画しない
			if (i + MapDrawPointX < 0 || j + MapDrawPointY < 0 ||
				i + MapDrawPointX >= MAPCHIPWIDTH || j + MapDrawPointY >= MAPCHIPHEIGHT) continue;

			if (map->stage[j + MapDrawPointY][i + MapDrawPointX] == 1)
			{
				DrawExtendGraph
				(
					(float)i * MAPCHIPSIZE + ScrollX,
					(float)j * MAPCHIPSIZE + ScrollY,
					(float)i * MAPCHIPSIZE + MAPCHIPSIZE + ScrollX,
					(float)j * MAPCHIPSIZE + MAPCHIPSIZE + ScrollY,
					resource->WallHandle, FALSE
				);
			}
			else
			{
				DrawExtendGraph
				(
					(float)i * MAPCHIPSIZE + ScrollX,
					(float)j * MAPCHIPSIZE + ScrollY,
					(float)i * MAPCHIPSIZE + MAPCHIPSIZE + ScrollX,
					(float)j * MAPCHIPSIZE + MAPCHIPSIZE + ScrollY,
					resource->BrickHandle, FALSE
				);
			}
			//DrawFormatString
			//(
			//	(float)i * MAPCHIPSIZE + ScrollX,
			//	(float)j * MAPCHIPSIZE + ScrollY,
			//	GetColor(255, 0, 0),
			//	"%d %d", j + MapDrawPointY,i + MapDrawPointX
			//);
		}
	}

	return;
}

